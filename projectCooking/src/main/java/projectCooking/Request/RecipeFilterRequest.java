package projectCooking.Request;

public class RecipeFilterRequest {
    private String status; // DRAFT, PENDING, APPROVED, REJECTED
    private String category;
    private String difficulty; // EASY, MEDIUM, HARD
    private String searchTerm; // Search by title
    private Integer userId; // Filter by user
    private Integer page = 0;
    private Integer size = 20;
    private String sortBy = "createdAt"; // createdAt, title, viewCount, likeCount
    private String sortDirection = "DESC"; // ASC, DESC

    public RecipeFilterRequest() {
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getCategory() {
        return category;
    }

    public void setCategory(String category) {
        this.category = category;
    }

    public String getDifficulty() {
        return difficulty;
    }

    public void setDifficulty(String difficulty) {
        this.difficulty = difficulty;
    }

    public String getSearchTerm() {
        return searchTerm;
    }

    public void setSearchTerm(String searchTerm) {
        this.searchTerm = searchTerm;
    }

    public Integer getUserId() {
        return userId;
    }

    public void setUserId(Integer userId) {
        this.userId = userId;
    }

    public Integer getPage() {
        return page;
    }

    public void setPage(Integer page) {
        this.page = page;
    }

    public Integer getSize() {
        return size;
    }

    public void setSize(Integer size) {
        this.size = size;
    }

    public String getSortBy() {
        return sortBy;
    }

    public void setSortBy(String sortBy) {
        this.sortBy = sortBy;
    }

    public String getSortDirection() {
        return sortDirection;
    }

    public void setSortDirection(String sortDirection) {
        this.sortDirection = sortDirection;
    }
}
